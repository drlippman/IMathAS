<h3 style="color: #000099">Rights</h3>

<h5>When adding a new administrator or changing rights, there are several rights levels, each higher level including the rights of the lower levels:
</h5>
    <h5>1.<b>Guest</b>: Can access all class materials, including taking tests (however, test is restarted next time the guest user accesses the test).Cannot enroll or unenroll in courses, or change
       user info or password.  Cannot post in forums</h5>
    <h5>2.<b>Student</b>: Can only access class materials - cannot edit anything</h5>
    <h5>3.<b>Teacher</b>: Can edit course materials and create assessments, but only in courses
	to which they have been assigned as a teacher</h5>
    <h5>4.<b>Limited Course Creator</b>: Can add courses, and they are automatically assigned as the teacher.  Can delete courses
       that they create.</h5>
    <h5>5.<b>Diagnostic Creator</b>: Can add courses, and they are automatically assigned as the teacher.  Can delete courses
       that they create.  Can create diagnostics.</h5>
    <h5>6.<b>Group Admin</b>: Can add/delete teachers and set user rights, but only for users in their group.  Can modify/delete questions
and libraries created by members of the group regardless of ownership or use rights.  Can always create "open to all" libraries</h5>
    <h5>7.<b>Full Administrator</b>: Can add/delete administrators and set user rights.  Can import macro files, if installation allows.
Can modify/delete questions and libraries regardless of ownership or use rights</h5>


